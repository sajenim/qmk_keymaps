oled_rotation_t oled_init_user(oled_rotation_t rotation) {
  if (is_keyboard_master()) {
    return OLED_ROTATION_270;
  } else {
    return OLED_ROTATION_270;
  }
  return rotation;
}

void oled_render_master(void) {
  // Default layer
  switch (get_highest_layer(default_layer_state)) {
  case L_QWERTY:
    oled_write("QWRTY", false);
    break;
  case L_CANARY:
    oled_write("CANRY", false);
    break;
  default:
    oled_write("UNDEF", false);
  }

  // Feature Status
  oled_write("-----", false);

  bool autoshift_enabled = get_autoshift_state();
  if (autoshift_enabled) {
    oled_write("as: *", false);
  } else {
    oled_write("as: .", false);
  }

  bool autocorrect_enabled = autocorrect_is_enabled();
  if (autocorrect_enabled) {
    oled_write("ac: *", false);
  } else {
    oled_write("ac: .", false);
  }

  bool combo_enabled = is_combo_enabled();
  if (combo_enabled) {
    oled_write("cm: *", false);
  } else {
    oled_write("cm: .", false);
  }

  bool sentence_case_enabled = is_sentence_case_on();
  if (sentence_case_enabled) {
    oled_write("sc: *", false);
  } else {
    oled_write("sc: .", false);
  }

  bool caps_word_enabled = is_caps_word_on();
  if (caps_word_enabled) {
    oled_write("cw: *", false);
  } else {
    oled_write("cw: .", false);
  }

  // Render Pet
  render_luna(0, 13);
}

void oled_render_slave(void) {
  // Layer Status
  switch (get_highest_layer(layer_state)) {
  case L_QWERTY:
  case L_CANARY:
    oled_write("DEFLT", false);
    break;
  case L_LOWER:
    oled_write("LOWER", false);
    break;
  case L_RAISE:
    oled_write("RAISE", false);
    break;
  case L_ADJUST:
    oled_write("ADJST", false);
    break;
  case L_EXTEND:
    oled_write("EXTND", false);
    break;
  case L_GAMING:
  case L_GAMEFN:
    oled_write("GAMES", false);
    break;
  default:
    oled_write("UNDEF", false);
  }

  // Mod Status
  uint8_t this_mod = get_mods();
  uint8_t this_osm = get_oneshot_mods();

  oled_write("-----", false);

  if ((this_mod | this_osm) & MOD_MASK_GUI) {
    oled_write("gui:*", false);
  } else {
    oled_write("gui:.", false);
  }

  if ((this_mod | this_osm) & MOD_MASK_ALT) {
    oled_write("alt:*", false);
  } else {
    oled_write("alt:.", false);
  }

  if ((this_mod | this_osm) & MOD_MASK_SHIFT) {
    oled_write("sft:*", false);
  } else {
    oled_write("sft:.", false);
  }

  if ((this_mod | this_osm) & MOD_MASK_CTRL) {
    oled_write("ctl:*", false);
  } else {
    oled_write("ctl:.", false);
  }

  // WPM Status
  char wpm_str[4];
  sprintf(wpm_str, "%03d", get_current_wpm());
  oled_set_cursor(1, 14);
  oled_write_ln(wpm_str, false);
  oled_write(" wpm", false);
}

bool oled_task_user(void) {
  current_wpm = get_current_wpm();
  led_usb_state = host_keyboard_led_state();

  if (is_keyboard_master()) {
    oled_render_master();
  } else {
    oled_render_slave();
  }
  return false;
}
